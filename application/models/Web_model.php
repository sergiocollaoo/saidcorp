<?php if ( ! defined('BASEPATH')) exit('No direct script access allowed');

class web_model extends CI_Model {
/************************************************************************************************************************************************************************/
public function view_ciiu($data)
    {
        $Codciiu  = $data['codciiu'];
        $query=$this->db->query("CALL sp_view_ciiu('$Codciiu')");
        if ($query->num_rows()>0)
        {
            return $query->result();
        }
        else
        {
            return false;
        } 
    }
/************************************************************************************************************************************************************************/

/************************************************************************************************************************************************************************/
}